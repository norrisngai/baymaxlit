def gemini_reply(
    *,
    client: Any,
    types: Any,
    model_name: str,
    user: dict[str, Any],
    chat_id: int,
    user_text: str,
    use_web: bool = False,
    upload_text: str | None = None,
    upload_image_bytes: bytes | None = None,
    upload_image_mime: str | None = None,
) -> str:
    if not client or not types:
        return "Gemini is not configured yet. Set GOOGLE_GEMINI_API_KEY in your environment."  # no secrets in code

    msgs = web_db.list_messages(chat_id=chat_id, limit=50)
    contents = []
    for m in msgs:
        role = "user" if m.get("role") == "user" else "model"
        contents.append(types.Content(role=role, parts=[types.Part(text=m.get("content") or "")]))

    # Optional: add web search/page context (Tavily) ONLY when explicitly requested by the user.
    if use_web and web_tavily is not None:
        try:
            url_match = re.search(r"https?://\S+", user_text or "", flags=re.IGNORECASE)
            if url_match:
                url = url_match.group(0).rstrip(")]}>,.\"' ")
                page_text = web_tavily.fetch_page_text(url)
                if page_text:
                    contents.append(
                        types.Content(
                            role="user",
                            parts=[
                                types.Part(
                                    text=(
                                        "User provided a webpage URL. Use the following extracted page text to answer or summarize.\n\n"
                                        f"URL: {url}\n\n"
                                        f"{page_text}"
                                    )
                                )
                            ],
                        )
                    )

            web_ctx = web_tavily.build_web_context(user_question=user_text)
        except Exception:
            web_ctx = ""
        if web_ctx:
            contents.append(types.Content(role="user", parts=[types.Part(text=web_ctx)]))

    user_parts: list[Any] = []
    if upload_text:
        # Put extracted text before the user's question.
        try:
            user_parts.append(types.Part.from_text(upload_text))
        except Exception:
            user_parts.append(types.Part(text=upload_text))

    if upload_image_bytes:
        mime = (upload_image_mime or "image/png").strip() or "image/png"
        try:
            user_parts.append(types.Part.from_text("[User uploaded an image. Use it to answer.]"))
            user_parts.append(types.Part.from_bytes(data=upload_image_bytes, mime_type=mime))
        except Exception:
            # If the SDK/model doesn't support image parts, fall back gracefully.
            user_parts.append(types.Part(text="[User uploaded an image so I can describe it, but this model may not support vision in this app configuration.]"))

    try:
        user_parts.append(types.Part.from_text(user_text))
    except Exception:
        user_parts.append(types.Part(text=user_text))

    contents.append(types.Content(role="user", parts=user_parts))

    system_instruction = build_system_prompt(user)
    try:
        chat_row = web_db.get_chat(chat_id=int(chat_id), user_id=int(user.get("id") or 0))
    except Exception:
        chat_row = None

    if chat_row and str(chat_row.get("chat_type") or "") == "revision":
        system_instruction += (
            "\n\nRevision mode instructions:\n"
            "- If the chat contains a 'Revision plan' section, follow it session-by-session.\n"
            "- Keep track of what subtopics have been covered in this chat and propose the next subtopic.\n"
            "- Focus on one subtopic at a time: explain, then give short practice questions, then answers/solutions.\n"
            "- If the user says they finished a session, move to the next session in the plan.\n"
        )

    try:
        resp = client.models.generate_content(
            model=model_name,
            contents=contents,
            config=types.GenerateContentConfig(
                system_instruction=system_instruction,
                temperature=0.7,
                max_output_tokens=2048,
            ),
        )
        return (resp.text or "").strip() or "(no response)"
    except Exception as e:
        # Avoid hard-crashing the web app; surface a concise error.
        return f"Gemini request failed: {e}"
