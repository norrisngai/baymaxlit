    upcoming: list[dict[str, Any]] = []
    class_level = (user.get("class_level") or "").strip()
    if class_level:
        try:
            upcoming = web_db.get_upcoming_assignments_for_class(target_class=class_level, limit=20)
        except Exception:
            upcoming = []

    upcoming = web_context.filter_assignments_for_student(user=user, assignments=upcoming)

