def _adapt_sql(sql: str) -> str:
    if not USE_POSTGRES:
        return sql

    sql = re.sub(
        r"INTEGER\s+PRIMARY\s+KEY\s+AUTOINCREMENT",
        "INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY",
        sql,
        flags=re.IGNORECASE,
    )
    return sql.replace("?", "%s")


def _extract_insert_table(sql: str) -> Optional[str]:
    m = re.match(r"\s*INSERT\s+INTO\s+([a-zA-Z_][\w]*)", sql, re.IGNORECASE)
    if not m:
        return None
    return m.group(1).lower()


class _CompatCursor:
    def __init__(self, inner: Any):
        self._inner = inner
        self.lastrowid: Optional[int] = None

    def execute(self, sql: str, params: Any = None):
        sql_text = _adapt_sql(sql)
        values = tuple(params or ())
        self.lastrowid = None

        insert_table = _extract_insert_table(sql_text)
        wants_returning = (
            USE_POSTGRES
            and insert_table in _PG_TABLES_WITH_ID
            and " returning " not in sql_text.lower()
        )
        if wants_returning:
            sql_text = sql_text.rstrip().rstrip(";") + " RETURNING id"

        self._inner.execute(sql_text, values)

        if wants_returning:
            row = self._inner.fetchone()
            if row is not None:
                try:
                    self.lastrowid = int(row["id"])
                except Exception:
                    try:
                        self.lastrowid = int(row[0])
                    except Exception:
                        self.lastrowid = None
        else:
            try:
                raw_lastrowid = getattr(self._inner, "lastrowid", None)
                if raw_lastrowid is not None:
                    self.lastrowid = int(raw_lastrowid)
            except Exception:
                self.lastrowid = None

        return self

    def executemany(self, sql: str, seq_of_params: Any):
        self.lastrowid = None
        self._inner.executemany(_adapt_sql(sql), seq_of_params)
        return self

    def executescript(self, script: str):
        if not USE_POSTGRES:
            self._inner.executescript(script)
            return self

        statements = [stmt.strip() for stmt in script.split(";") if stmt.strip()]
        for stmt in statements:
            self._inner.execute(_adapt_sql(stmt))
        return self

    def fetchone(self):
        return self._inner.fetchone()

    def fetchall(self):
        return self._inner.fetchall()

    @property
    def rowcount(self) -> int:
        try:
            return int(self._inner.rowcount or 0)
        except Exception:
            return 0

    def __iter__(self):
        return iter(self._inner)

    def __getattr__(self, name: str):
        return getattr(self._inner, name)


class _CompatConnection:
    def __init__(self, inner: Any):
        self._inner = inner

    def cursor(self):
        return _CompatCursor(self._inner.cursor())

    def commit(self):
        return self._inner.commit()

    def rollback(self):
        return self._inner.rollback()

    def close(self):
        return self._inner.close()

    def __enter__(self):
        self._inner.__enter__()
        return self

    def __exit__(self, exc_type, exc, tb):
        return self._inner.__exit__(exc_type, exc, tb)

    def __getattr__(self, name: str):
        return getattr(self._inner, name)


def _connect() -> Any:
    if USE_POSTGRES:
        if psycopg is None or dict_row is None:
            raise RuntimeError("DATABASE_URL is set but psycopg is not installed.")
        conn = psycopg.connect(DATABASE_URL, row_factory=dict_row)
        return _CompatConnection(conn)

    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    return _CompatConnection(conn)


