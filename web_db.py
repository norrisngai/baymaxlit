def _normalize_task_type(value: str) -> str:
    v = (value or "").strip().lower()
    if v in {"homework", "hw"}:
        return "homework"
    if v in {"quiz", "quizz"}:
        return "quiz"
    if v == "test":
        return "test"
    if v in {"exam", "exams"}:
        return "exam"
    if v == "activity":
        return "activity"
    return "study"


